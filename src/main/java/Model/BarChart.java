package Model;

public class BarChart {
    private Number o;
    private Number h;
    private Number l;
    private Number c;
    private Number volume;
    private String event;
    private String symbol;
    private Number barNum;

    public Number getO ( ) {
        return o;
    }

    public void setO (Number o) {
        this.o = o;
    }

    public Number getH ( ) {
        return h;
    }

    public void setH (Number h) {
        this.h = h;
    }

    public Number getL ( ) {
        return l;
    }

    public void setL (Number l) {
        this.l = l;
    }

    public Number getC ( ) {
        return c;
    }

    public void setC (Number c) {
        this.c = c;
    }

    public Number getVolume ( ) {
        return volume;
    }

    public void setVolume (Number volume) {
        this.volume = volume;
    }

    public String getEvent ( ) {
        return event;
    }

    public void setEvent (String event) {
        this.event = event;
    }

    public String getSymbol ( ) {
        return symbol;
    }

    public void setSymbol (String symbol) {
        this.symbol = symbol;
    }

    public Number getBarNum ( ) {
        return barNum;
    }

    public void setBarNum (Number barNum) {
        this.barNum = barNum;
    }
}
